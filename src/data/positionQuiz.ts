export function matchesPattern(answerIds: string[], pattern: string[]): boolean {
  if (answerIds.length !== pattern.length) return false;
  return pattern.every((id) => answerIds.includes(id));
}

export function getPurePosition(answerIds: string[]): keyof PositionScores | null {
  if (matchesPattern(answerIds, pureCarryPattern)) return 'pos1';
  if (matchesPattern(answerIds, pureMidPattern)) return 'pos2';
  if (matchesPattern(answerIds, pureOfflanePattern)) return 'pos3';
  if (matchesPattern(answerIds, pureFullSupportPattern)) return 'pos5';
  return null;
}

